VOICES = {"marina": MARINA, "maxim": MAXIM}

def generate(file_id, voice_id, text):
    path = os.path.join(OUT_DIR, f"{file_id}.mp3")
    if os.path.exists(path):
        print(f"  ✓ уже есть: {file_id}")
        return True
    url = f"https://api.elevenlabs.io/v1/text-to-speech/{voice_id}"
    headers = {"xi-api-key": API_KEY, "Content-Type": "application/json"}
    body = {
        "text": text,
        "model_id": MODEL,
        "voice_settings": {
            "stability": 0.78,
            "similarity_boost": 0.85,
            "style": 0.15,
            "speed": 0.78
        }
    }
    r = requests.post(url, headers=headers, json=body)
    if r.status_code == 200:
        with open(path, "wb") as f:
            f.write(r.content)
        print(f"  ✓ {file_id}  ({len(r.content)//1024} kb)")
        return True
    else:
        print(f"  ✗ {file_id}: {r.status_code} — {r.text[:120]}")
        return False

if __name__ == "__main__":
    os.makedirs(OUT_DIR, exist_ok=True)
    total = len(SENTENCES) * len(VOICES)
    ok = 0
    i = 0
    print(f"Генерируем {total} файлов (каждый сатз × 2 голоса)...\n")
    for base_id, text in SENTENCES:
        for voice_name, voice_id in VOICES.items():
            i += 1
            file_id = f"{base_id}_{voice_name}"
            print(f"[{i}/{total}]", end=" ")
            if generate(file_id, voice_id, text):
                ok += 1
            time.sleep(0.4)
    print(f"\n{'='*40}")
    print(f"Готово: {ok}/{total} файлов в папке ./{OUT_DIR}/")
